#Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
n = int(input('Digite um número: '))
print(f' O dobro de {n} é {n*2},\n o triplo de {n} é {n*3:.2f},\n e a raiz quadrada de {n} é {n**(1/2):.2f}')

